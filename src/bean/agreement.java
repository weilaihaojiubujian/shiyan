package bean;

public class agreement {


	   private String agreementname;
	   private String agreementintroduce;
	   private int aid;
	public String getAgreementname() {
		return agreementname;
	}
	public void setAgreementname(String agreementname) {
		this.agreementname = agreementname;
	}
	public String getAgreementintroduce() {
		return agreementintroduce;
	}
	public void setAgreementintroduce(String agreementintroduce) {
		this.agreementintroduce = agreementintroduce;
	}
	public int getAid() {
		return aid;
	}
	public void setAid(int aid) {
		this.aid = aid;
	}
	   
}
